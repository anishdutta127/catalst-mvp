'use client';

import { useState, useEffect, useRef, useCallback } from 'react';
import { motion } from 'framer-motion';
import { useJourneyStore } from '@/lib/store/journeyStore';
import { useUIStore } from '@/lib/store/uiStore';
import { lines } from '@/content/lines';
import { analytics } from '@/lib/analytics';
import housesRaw from '@/content/houses.json';
import type { ScoredIdea } from '@/lib/scoring/types';
import { ScreenQuote } from '@/components/ui/ScreenQuote';
import { MysticVaultCard } from '@/components/ui/MysticVaultCard';

interface LineageFigure { name: string; sharedTraitLine: string; quantified_impact?: string; epithet?: string }
interface House {
  id: string; name: string; hex: string; tagline: string;
  description: string; strengths: string[]; lineage: LineageFigure[];
  collective_impact?: string;
}
const HOUSES = housesRaw as unknown as House[];

const ORB_COLORS: Record<string, string> = {
  Grit: '#F59E0B', Vision: '#F0D060', Craft: '#CD7F32', Influence: '#9B59B6',
  Empathy: '#00D8B9', Analysis: '#5DADE2', Freedom: '#BDC3C7', Stability: '#27AE60',
};

/**
 * S11 — Founder Profile (Batch 3 update).
 *
 * Changes vs previous:
 *   - Replaces the old "Want expert help?" toggle + collapsible consultation block
 *     with the MysticVaultCard variant="full" — always visible at the bottom.
 *   - Removes aboutExpanded state + showConsultation state (no longer needed).
 *   - Keeps founder card, house identity, top 3 ideas, share row, downloads exactly
 *     as they were. Batch 4 will replace the founder card with the Trading Card.
 */
export function S11Profile() {
  const state = useJourneyStore();
  const enqueueMessage = useUIStore((s) => s.enqueueMessage);

  const [downloadError, setDownloadError] = useState('');
  const [cardSaving, setCardSaving] = useState(false);
  const dialogueSent = useRef(false);
  const cardRef = useRef<HTMLDivElement>(null);

  const house = HOUSES.find((h) => h.id === state.houseId) || HOUSES[0];
  const crowned = state.matchedIdeas
    ? [state.matchedIdeas.nest, state.matchedIdeas.spark, state.matchedIdeas.wildvine]
        .find((s) => s.idea.idea_id === state.crownedIdeaId) || state.matchedIdeas.nest
    : null;
  const allIdeas = state.matchedIdeas
    ? [state.matchedIdeas.nest, state.matchedIdeas.spark, state.matchedIdeas.wildvine]
    : [];
  const displayName = state.displayName || 'Founder';
  const orbs = state.crystalOrbs.slice(0, 3);
  const orbColors = orbs.map((o) => ORB_COLORS[o] || '#D4A843');

  useEffect(() => {
    if (dialogueSent.current) return;
    dialogueSent.current = true;
    analytics.complete(state.houseId || 'unknown', crowned?.idea.idea_name || 'unknown');
    enqueueMessage({ speaker: 'cedric', text: lines.s11.cedric.intro, type: 'dialogue' });
    setTimeout(() => enqueueMessage({ speaker: 'cedric', text: lines.s11.cedric.final(displayName), type: 'dialogue' }), 3000);
    setTimeout(() => enqueueMessage({ speaker: 'pip', text: lines.s11.pip.final, type: 'dialogue' }), 5000);
  }, [enqueueMessage, displayName, state.houseId, crowned]);

  const generateMarkdown = useCallback(() => {
    const whyYou = crowned ? (state.whyYouTexts[crowned.idea.idea_id] || 'Trust the match.') : '';
    const qs = crowned?.idea.quickStart;
    return `# ${crowned?.idea.idea_name || 'Your Idea'} — Your Catalst Founder Profile

**House:** ${house.name}
**Match:** ${crowned?.displayScore || 0}% in ${crowned?.idea.domain_primary.replace(/_/g, ' ') || 'unknown'}

## Your Top Idea
${crowned?.idea.idea_name || ''} — ${crowned?.idea.one_liner || ''}

${crowned?.idea.pain_to_promise || ''}

## Why This Fits You
${whyYou.replace('__loading__', 'Trust the match.')}

## First Steps
- Week 1: ${qs?.week1 || 'Validate the core assumption'}
- MVP: ${qs?.mvp || 'Build the simplest version'}
- First customers: ${qs?.firstCustomers || 'Talk to 10 people in the space'}

## Your Personality Profile
House: ${house.name} | Traits: ${orbs.join(', ')} | Strengths: ${house.strengths.join(', ')}

---
Generated by Catalst · catalst.app
`;
  }, [crowned, house, orbs, state.whyYouTexts]);

  function handleDownloadMd() {
    try {
      const md = generateMarkdown();
      const blob = new Blob([md], { type: 'text/markdown' });
      const url = URL.createObjectURL(blob);
      const a = document.createElement('a');
      a.href = url;
      a.download = `catalst-${house.id}-${displayName.toLowerCase().replace(/\s+/g, '-')}.md`;
      document.body.appendChild(a);
      a.click();
      document.body.removeChild(a);
      URL.revokeObjectURL(url);
      analytics.cta('download');
    } catch { setDownloadError('Download failed, try again'); }
  }

  async function handleSaveCard() {
    if (!cardRef.current) return;
    setCardSaving(true);
    try {
      const { toPng } = await import('html-to-image');
      const dataUrl = await toPng(cardRef.current, { pixelRatio: 2 });
      const a = document.createElement('a');
      a.href = dataUrl;
      a.download = `catalst-${house.id}-${displayName.toLowerCase().replace(/\s+/g, '-')}.png`;
      a.click();
      analytics.cta('download');
    } catch { setDownloadError('Card save failed, try again'); }
    setCardSaving(false);
  }

  return (
    <div className="flex flex-col gap-8 pb-8 overflow-y-auto">
      {/* ── Section 1: Founder Card (Batch 4 will replace with Trading Card) ── */}
      <div className="mx-auto w-[300px] sm:w-[400px]">
        <div
          ref={cardRef}
          data-testid="founder-card"
          className="rounded-2xl p-6 flex flex-col items-center gap-3 text-center"
          style={{ border: `3px solid ${house.hex}`, background: '#14171E', aspectRatio: '9 / 16', boxShadow: `0 0 20px ${house.hex}30` }}
        >
          <p className="text-sm font-serif italic" style={{ color: house.hex }}>{house.name}</p>

          {/* Crystal triangle visual */}
          <svg viewBox="0 0 100 80" className="w-28 h-20">
            {orbs.length >= 2 && <line x1="50" y1="15" x2="25" y2="60" stroke={orbColors[0]} strokeWidth="1" opacity="0.4" />}
            {orbs.length >= 2 && <line x1="50" y1="15" x2="75" y2="60" stroke={orbColors[1]} strokeWidth="1" opacity="0.4" />}
            {orbs.length >= 3 && <line x1="25" y1="60" x2="75" y2="60" stroke={orbColors[2]} strokeWidth="1" opacity="0.4" />}
            {orbs[0] && <circle cx="50" cy="15" r="7" fill={orbColors[0]} opacity="0.85" />}
            {orbs[1] && <circle cx="25" cy="60" r="7" fill={orbColors[1]} opacity="0.85" />}
            {orbs[2] && <circle cx="75" cy="60" r="7" fill={orbColors[2]} opacity="0.85" />}
            <circle cx="50" cy="45" r="3" fill="#D4A843" opacity="0.5">
              <animate attributeName="r" values="3;5;3" dur="3s" repeatCount="indefinite" />
            </circle>
          </svg>

          <p className="text-[10px] uppercase tracking-widest" style={{ color: house.hex, opacity: 0.9 }}>
            {crowned?.displayScore || 0}% MATCH · {crowned?.idea.domain_primary.replace(/_/g, ' ').toUpperCase() || ''}
          </p>
          <p className="text-[13px] text-ivory/60 italic leading-snug max-w-[80%]">{house.tagline}</p>

          <div className="text-center space-y-1">
            <p className="text-[9px] text-gold/60 uppercase tracking-widest">Your lineage</p>
            <p className="text-[10px] text-ivory/30">{house.lineage.map((f) => f.name).join(' · ')}</p>
            <p className="text-[11px] text-ivory/40 italic">
              {house.collective_impact || house.tagline}
            </p>
          </div>

          {crowned && (
            <div className="text-center space-y-0.5">
              <p className="text-[9px] text-gold/60 uppercase tracking-widest">Your top match</p>
              <p className="text-[12px] text-ivory font-semibold">{crowned.idea.idea_name} <span className="text-gold text-[10px]">{crowned.displayScore}%</span></p>
              <p className="text-[10px] text-ivory/30">+ 2 more ideas waiting</p>
            </div>
          )}

          <p className="text-[9px] text-ivory/20 mt-auto">catalst.app</p>
        </div>
      </div>

      {/* ── Section 2: House identity ── */}
      <div className="space-y-3">
        <h3 className="text-2xl font-serif font-bold" style={{ color: house.hex }}>{house.name}</h3>
        <p className="text-sm text-ivory-muted leading-relaxed">{house.description}</p>
        <div className="space-y-2">
          {house.lineage.map((fig) => (
            <div key={fig.name} className="bg-dark-surface border border-white/10 rounded-lg p-3">
              <p className="text-sm font-semibold text-ivory">{fig.name}</p>
              <p className="text-[10px] text-ivory/40">{fig.sharedTraitLine}</p>
            </div>
          ))}
        </div>
      </div>

      {/* ── Section 3: Top 3 Ideas ── */}
      <div className="space-y-2">
        <p className="text-xs font-mono text-ivory/30 uppercase tracking-widest">Your matched ideas</p>
        {allIdeas.map((si: ScoredIdea) => {
          const isCrowned = si.idea.idea_id === state.crownedIdeaId;
          return (
            <div key={si.idea.idea_id} className={`bg-dark-surface rounded-lg p-3 ${isCrowned ? 'border-2 border-gold/50' : 'border border-white/10'}`}>
              <div className="flex items-center justify-between">
                <p className={`text-sm font-semibold ${isCrowned ? 'text-gold' : 'text-ivory'}`}>{si.idea.idea_name}</p>
                <span className="text-[10px] font-mono text-ivory/30">{si.displayScore}%</span>
              </div>
              <p className="text-xs text-ivory/40 mt-1 line-clamp-1">{si.idea.one_liner}</p>
            </div>
          );
        })}
      </div>

      {/* ── Section 4: Share + Downloads ── */}
      <div className="space-y-3 max-w-sm mx-auto w-full">
        <div className="grid grid-cols-3 gap-2">
          <motion.button
            whileTap={{ scale: 0.94 }}
            onClick={() => {
              const t = encodeURIComponent('Card saved! Open Instagram Stories and upload from gallery 📱');
              window.open(`https://wa.me/?text=${t}`, '_blank');
              handleSaveCard();
            }}
            className="h-11 rounded-xl text-white text-[12px] font-medium bg-gradient-to-br from-purple-500/80 to-pink-500/80 flex items-center justify-center gap-1"
          >
            📸 Instagram
          </motion.button>
          <motion.button
            whileTap={{ scale: 0.94 }}
            onClick={() => {
              const t = encodeURIComponent(`Just found my startup idea on Catalst 🌱\nI'm a ${house.name} founder. Match: ${crowned?.idea.idea_name || 'my idea'} at ${crowned?.displayScore || 0}% fit.\n\ncatalst.app`);
              window.open(`https://twitter.com/intent/tweet?text=${t}`, '_blank');
            }}
            className="h-11 rounded-xl text-white text-[12px] font-medium bg-black/60 border border-white/15 flex items-center justify-center gap-1"
          >
            𝕏 Twitter
          </motion.button>
          <motion.button
            whileTap={{ scale: 0.94 }}
            onClick={() => {
              const t = encodeURIComponent(`I just discovered my startup idea on Catalst! 🌱\n\nI'm a ${house.name} founder. My top match: *${crowned?.idea.idea_name || 'my idea'}* at ${crowned?.displayScore || 0}% fit.\n\nWhat founder are you? → catalst.app`);
              window.open(`https://wa.me/?text=${t}`, '_blank');
              analytics.cta('whatsapp');
            }}
            className="h-11 rounded-xl text-white text-[12px] font-medium bg-green-600/80 flex items-center justify-center gap-1"
          >
            💬 WhatsApp
          </motion.button>
        </div>

        <div className="flex gap-2">
          <button
            onClick={handleSaveCard}
            disabled={cardSaving}
            className="flex-1 h-11 rounded-xl bg-white/6 border border-white/12 text-ivory/70 text-[13px] hover:bg-white/10 transition-all flex items-center justify-center gap-1"
          >
            {cardSaving ? 'Saving...' : '🖼 Save Card'}
          </button>
          <button
            onClick={handleDownloadMd}
            data-testid="download-btn"
            className="flex-1 h-11 rounded-xl bg-white/6 border border-white/12 text-ivory/70 text-[13px] hover:bg-white/10 transition-all flex items-center justify-center gap-1"
          >
            📄 Idea Pack
          </button>
        </div>
        {downloadError && <p className="text-xs text-error text-center">{downloadError}</p>}
      </div>

      {/* ── Section 5: Mystic Vault (REPLACES old consultation toggle) ── */}
      <MysticVaultCard
        matchPercent={crowned?.displayScore}
        ideaName={crowned?.idea.idea_name}
        houseName={house.name}
        variant="full"
      />

      <ScreenQuote screen="s11" />

      <p className="text-[10px] text-ivory/20 text-center font-mono">
        {lines.s11.journeyComplete(house.name)}
      </p>
    </div>
  );
}
